package beans;

public class LearningMaterialBean 
{
   private int id;
   private String className,subject,chepter,url,type,fileName,title;
   public LearningMaterialBean()
   {
	   
   }
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getClassName() {
	return className;
}
public void setClassName(String className) {
	this.className = className;
}
public String getSubject() {
	return subject;
}
public void setSubject(String subject) {
	this.subject = subject;
}
public String getChepter() {
	return chepter;
}
public void setChepter(String chepter) {
	this.chepter = chepter;
}
public String getUrl() {
	return url;
}
public void setUrl(String url) {
	this.url = url;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public String getFileName() {
	return fileName;
}
public void setFileName(String fileName) {
	this.fileName = fileName;
}
public String getTitle() {
	return title;
}
public void setTitle(String title) {
	this.title = title;
}
   
}
